def _build_unreliable_feedback(prediction: ModelPrediction) -> ResearchFeedback:
    evidence = []
    if prediction.candidate_class:
        evidence.append(
            "Наиболее вероятная гипотеза до проверки надёжности: "
            f"{prediction.candidate_class}."
        )
    evidence.append(
        f"Максимальная оценка класса: {prediction.confidence * 100:.1f}%."
    )
    evidence.extend(prediction.reliability_warnings)

    return ResearchFeedback(
        title="Недостаточно данных для надёжной диагностической гипотезы",
        explanation=(
            "Модель не выдаёт итоговую классификацию: входные данные выходят "
            "за обучающий диапазон или несколько гипотез имеют близкие оценки."
        ),
        evidence=evidence,
        student_question=(
            "Какие особенности измерений, единиц или экспериментальной установки "
            "могли привести к неоднозначному результату?"
        ),
        recommended_action=(
            "Проверьте структуру файла, единицы измерения и условия опыта. "
            "После проверки повторите анализ или обсудите серию с преподавателем."
        ),
        teacher_note=(
            "Автоматическая классификация отклонена механизмом контроля надёжности."
        ),
        requires_repeat=True,
    )


def build_feedback(prediction: ModelPrediction) -> ResearchFeedback:
    if not prediction.accepted or prediction.predicted_class == "unknown":
        return _build_unreliable_feedback(prediction)

    lab_rules = RULES.get(prediction.lab_id)
    if lab_rules is None or prediction.predicted_class not in lab_rules:
        raise KeyError(
            f"No recommendation rule for {prediction.lab_id}/{prediction.predicted_class}"
        )

    title, explanation, question, action, repeat = lab_rules[
        prediction.predicted_class
    ]
    evidence = [
        f"Определённость диагностической гипотезы: {prediction.confidence * 100:.1f}%."
    ]
    for name, value in sorted(
        prediction.features.items(),
        key=lambda item: abs(item[1]),
        reverse=True,
    )[:3]:
        evidence.append(f"{name}: {value:.4f}")

    return ResearchFeedback(
        title=title,
        explanation=explanation,
        evidence=evidence,
        student_question=question,
        recommended_action=action,
        teacher_note="Рекомендуется проверка преподавателем.",
        requires_repeat=repeat,
    )
